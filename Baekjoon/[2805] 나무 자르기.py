import sys



N,M =map(int, sys.stdin.readline().split())
tree = list(map(int, sys.stdin.readline().split()))
tree.sort(reverse = True) 

start = 0
end = max(tree) - 1

while (start < end):
    mid = (start + end)//2
    sum = 0

    for t in tree:
        if t - mid >= 0:
            sum += t - mid
        else:
            break

    if sum == M:
        break
    elif sum < M:
        end = mid - 1
    else:
        start = mid + 1

print(mid)